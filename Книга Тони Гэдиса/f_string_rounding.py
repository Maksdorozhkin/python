# программа выводит на экран следующие числа в двух столбцах
num1 = 127.899
num2 = 3465.148
num3 = 3.776
num4 = 264.821
num5 = 88.947
num6 = 5694.889

# каждое число выводится в поле из 10 знаков \
# и округляется до 2 х знаков после точки
print(f"{num1:10.2f} {num2:10.2f} ")
print(f"{num3:10.2f} {num4:10.2f} ")
print(f"{num5:10.2f} {num6:10.2f} ")