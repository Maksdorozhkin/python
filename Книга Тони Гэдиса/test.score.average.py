# програма контрольное взвешивание
# получить три значения и выдать средний вес
test1 = float(input('Введите вес первой кавитации: '))
test2 = float(input('Введите вес второй кавитации: '))
test3 = float(input('Введите вес третьей кавитации: '))
result = (test1 + test2 + test3) / 3
print('Cредний вес: ', result)
